CREATE OR REPLACE FUNCTION "public"."hybrid_search"("in_query" "text", "in_embedding" "public"."vector", "in_match_count" integer DEFAULT 3, "full_text_weight" double precision DEFAULT 1.0, "semantic_weight" double precision DEFAULT 1.0, "rrf_k" integer DEFAULT 50) RETURNS TABLE("id" "text", "source" "text", "source_id" "text", "document_id" "text", "url" "text", "created_at" timestamp with time zone, "author" "text", "content" "text", "embedding" "public"."vector", "rank" double precision)
    LANGUAGE "plpgsql"
    AS $$
BEGIN
    RETURN QUERY
    WITH full_text_search AS (
        SELECT
            documents.id,
            ROW_NUMBER() OVER (ORDER BY ts_rank(to_tsvector('english', documents.content), plainto_tsquery(in_query)) DESC) AS rank
        FROM documents
        WHERE to_tsvector('english', documents.content) @@ plainto_tsquery(in_query)
        ORDER BY rank
        LIMIT rrf_k
    ),
    semantic_search AS (
        SELECT
            documents.id,
            ROW_NUMBER() OVER (ORDER BY (documents.embedding <#> in_embedding) DESC) AS rank
        FROM documents
        WHERE (documents.embedding <#> in_embedding) > 0.5
        ORDER BY rank
        LIMIT rrf_k
    )
    SELECT
        d.id,
        d.source,
        d.source_id,
        d.document_id,
        d.url,
        d.created_at,
        d.author,
        d.content,
        d.embedding,
        (COALESCE(fts.rank, 0) * full_text_weight + COALESCE(ss.rank, 0) * semantic_weight) AS rank
    FROM documents d
    LEFT JOIN full_text_search fts ON d.id = fts.id
    LEFT JOIN semantic_search ss ON d.id = ss.id
    WHERE fts.id IS NOT NULL OR ss.id IS NOT NULL
    ORDER BY rank DESC
    LIMIT in_match_count;
END;
$$;

