CREATE OR REPLACE FUNCTION "public"."match_page_sections"("in_embedding" "public"."vector", "in_match_count" integer DEFAULT 3, "in_document_id" "text" DEFAULT '%%'::"text", "in_source_id" "text" DEFAULT '%%'::"text", "in_source" "text" DEFAULT '%%'::"text", "in_author" "text" DEFAULT '%%'::"text", "in_start_date" timestamp with time zone DEFAULT '-infinity'::timestamp with time zone, "in_end_date" timestamp with time zone DEFAULT 'infinity'::timestamp with time zone) RETURNS TABLE("id" "text", "source" "text", "source_id" "text", "document_id" "text", "url" "text", "created_at" timestamp with time zone, "author" "text", "content" "text", "embedding" "public"."vector", "similarity" double precision)
    LANGUAGE "plpgsql"
    AS $$
#variable_conflict use_variable
begin
return query
select
    documents.id,
    documents.source,
    documents.source_id,
    documents.document_id,
    documents.url,
    documents.created_at,
    documents.author,
    documents.content,
    documents.embedding,
    (documents.embedding <#> in_embedding) * -1 as similarity
from documents

where in_start_date <= documents.created_at and 
    documents.created_at <= in_end_date and
    (documents.source_id like in_source_id or documents.source_id is null) and
    (documents.source like in_source or documents.source is null) and
    (documents.author like in_author or documents.author is null) and
    (documents.document_id like in_document_id or documents.document_id is null)

order by documents.embedding <#> in_embedding

limit in_match_count;
end;
$$;

